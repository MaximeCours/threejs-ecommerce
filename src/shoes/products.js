const closeCrocsContainer = document.getElementById("closeCrocsContainer")
const closeCrocsButton = document.getElementById('closeCrocsButton')
closeCrocsContainer.addEventListener('mouseenter', function() {
    closeCrocsButton.classList.toggle('hidden')
})
closeCrocsContainer.addEventListener('mouseleave', function() {
    closeCrocsButton.classList.toggle('hidden')
})
const closeCrocsDetails = document.getElementById("closeCrocsDetails")
const closeCrocsBack = document.getElementById("closeCrocsBack")
closeCrocsButton.addEventListener('click', function() {
    closeCrocsDetails.classList.remove('hidden')
})
closeCrocsBack.addEventListener('click', function() {
    closeCrocsDetails.classList.add('hidden')
})


